<template>
  <ion-page>
    <ion-header class="ion-hide-md-up">
      <ion-toolbar>
        <ion-title>Items</ion-title>
        <ion-buttons slot="start">
          <ion-back-button default-href="/catalogues"></ion-back-button>
        </ion-buttons>
      </ion-toolbar>
    </ion-header>
    <ion-content :fullscreen="true">
      <ion-toolbar>
        <ion-searchbar @ionInput="filterDisplayedItems($event.target.value)"></ion-searchbar>
      </ion-toolbar>
      <ion-list>
        <ion-list-header>
          {{ selectedCatalogue.name }}
        </ion-list-header>
        <ion-item v-for="item in displayedItems" :key="item.name">
          <ion-label>{{ item.name }}</ion-label>
        </ion-item>
      </ion-list>
    </ion-content>
  </ion-page>
</template>

<script lang="ts">
import {
  IonPage,
  IonList,
  IonSearchbar,
  IonContent,
  IonLabel,
  IonItem,
  IonHeader,
  IonToolbar,
  IonButtons,
  IonTitle,
  IonListHeader,
  IonBackButton,
} from '@ionic/vue';

import { useCatalogue } from '@/composables/useCatalogue';
import { onMounted } from '@vue/runtime-core';
import router from '@/router';

export default {
  name: 'Catalogue',
  components: {
    IonButtons,
    IonTitle,
    IonSearchbar,
    IonLabel,
    IonHeader,
    IonToolbar,
    IonItem,
    IonList,
    IonContent,
    IonPage,
    IonListHeader,
    IonBackButton,
  },
  setup() {
    const {
      selectedCatalogue,
      getCatalogue,
      displayedItems,
      filterDisplayedItems,
    } = useCatalogue();

    onMounted(async () => {
      try {
        const routeID = router.currentRoute.value.params.id as string;
        await getCatalogue(routeID);
      } catch (error) {
        console.log(error);
      }
    });

    return { selectedCatalogue, filterDisplayedItems, displayedItems };
  },
};
</script>
